def _set_run_color(run, hex_color: str) -> None:
    rgb = hex_color.lstrip("#")
    run.font.color.rgb = RGBColor(int(rgb[0:2], 16), int(rgb[2:4], 16), int(rgb[4:6], 16))


def _add_page_number(paragraph) -> None:
    """
    Inserts a live PAGE field into a paragraph. python-docx has no API for
    page numbers, so the Word field is built from raw OOXML elements — the
    standard fldChar(begin) / instrText(PAGE) / fldChar(end) sequence.
    """
    run = paragraph.add_run()
    begin = OxmlElement("w:fldChar")
    begin.set(qn("w:fldCharType"), "begin")
    instr = OxmlElement("w:instrText")
    instr.set(qn("xml:space"), "preserve")
    instr.text = "PAGE"
    end = OxmlElement("w:fldChar")
    end.set(qn("w:fldCharType"), "end")
    run._r.append(begin)
    run._r.append(instr)
    run._r.append(end)


def _new_document() -> Document:
    """
    A Document pre-configured to the FNA 670 formatting brief: 1-inch
    margins, 12 pt Times New Roman double-spaced body, an AI DRAFT banner
    in the page header, and a page number centred in the footer.
    """
    doc = Document()

    section = doc.sections[0]
    section.left_margin = Inches(1)
    section.right_margin = Inches(1)
    section.top_margin = Inches(1)
    section.bottom_margin = Inches(1)

    # Body text — Times New Roman 12 pt, double-spaced.
    normal = doc.styles["Normal"]
    normal.font.name = _BODY_FONT
    normal.font.size = Pt(12)
    normal.paragraph_format.line_spacing_rule = WD_LINE_SPACING.DOUBLE
    normal.paragraph_format.space_after = Pt(0)

    # AI DRAFT banner — repeated in the header so it appears on every page.
    header_para = section.header.paragraphs[0]
    header_para.alignment = WD_ALIGN_PARAGRAPH.CENTER
    head_run = header_para.add_run(_AI_DRAFT_BANNER_TEXT)
    head_run.bold = True
    head_run.font.name = _BODY_FONT
    head_run.font.size = Pt(10)
    _set_run_color(head_run, "#b45309")

    # Page number — centred in the footer.
    footer_para = section.footer.paragraphs[0]
    footer_para.alignment = WD_ALIGN_PARAGRAPH.CENTER
    _add_page_number(footer_para)

    return doc


def _add_title_lines(doc: Document, lines: list[str], *, big_first: bool = False) -> None:
    """Centred title block — the first line optionally rendered large."""
    for i, line in enumerate(lines):
        para = doc.add_paragraph()
        para.alignment = WD_ALIGN_PARAGRAPH.CENTER
        para.paragraph_format.line_spacing_rule = WD_LINE_SPACING.SINGLE
        run = para.add_run(line)
        run.font.name = _BODY_FONT
        run.bold = i == 0
        run.font.size = Pt(20 if (big_first and i == 0) else 12)


def _add_heading(doc: Document, text: str, *, size: int = 13) -> None:
    """A bold Times New Roman heading — kept on the body font, not Word's
    Heading styles, so the 12 pt Times New Roman brief holds throughout."""
    para = doc.add_paragraph()
    para.paragraph_format.line_spacing_rule = WD_LINE_SPACING.SINGLE
    para.paragraph_format.space_before = Pt(12)
    para.paragraph_format.space_after = Pt(4)
    run = para.add_run(text)
    run.bold = True
    run.font.name = _BODY_FONT
    run.font.size = Pt(size)


_MARKDOWN_BOLD_RE = re.compile(r"\*\*(?P<inner>[^*\n]+)\*\*")


# Brief markdown heading detection. The Academic Writer's
# narratives occasionally emit `# Title`, `## Section N`, and
# `### References` lines that fell through _add_body as literal
# text (the `#` characters wrote into <w:t> instead of being
# stripped and the paragraph styled as Heading N). This pattern
# fires line-by-line in _add_brief_body; the scope is intentionally
# narrow -- only brief narratives route through there.
_MD_HEADING_LINE_RE = re.compile(r"^(#{1,3})\s+(.+?)\s*$")

# June 28 2026 (Fix 1) -- LLM-generated section-restate headings
# the brief narrative occasionally leads with. The outer brief
# builder already emits Heading1 for each section
# ("1. Executive Summary"); if the LLM also opens its narrative
# with "## Section 1: Executive Summary" or "## Section 1: ...",
# we get a duplicate heading rendering. Match the SECTION pattern
# and skip the inline restate entirely.
#
# Tolerates the bold-wrapped form "## **Section 1: ...**" and
# the bold-only form "**Section 1: ...**" without a markdown
# heading prefix.
#
# June 28 2026 -- extended to match LETTER section labels too
# ("Section A:", "Section B:", "Section H:") so the appendix's
# A-H section restates are also stripped. The original regex
# only matched DIGITS (\d+), which covered the brief's 1-5
# sections but missed every appendix section restate.
_SECTION_RESTATE_RE = re.compile(
    r"^(?:\s*#{1,6}\s*)?(?:\*{0,2})\s*section\s*"
    r"(?:\d+|[A-H])"
    r"\s*[:\-\.]",
    re.IGNORECASE)

# June 28 2026 (Fix 2) -- strip leading/trailing markdown bold
# markers from a heading line. The LLM occasionally wraps the
# whole heading text in ** so the DOCX heading paragraph would
# render with literal ** characters; the heading style
# already applies bold weighting via its template setting.
_HEADING_BOLD_WRAP_RE = re.compile(r"^\*{1,3}|\*{1,3}$")

# June 28 2026 (Fix 10) -- APA references consolidation.
# Each per-section narrative the LLM emits has its own inline
# References / Bibliography block. APA format requires a SINGLE
# consolidated References section at the end of the document.
# These two helpers (a) split a section narrative into
# (body, references_lines) and (b) collect / dedupe / sort
# the citations across sections so the brief builder can emit
# one consolidated block at the end.
_REFS_HEADING_RE = re.compile(
    r"(?im)^(?:\s*#{1,6}\s*)?(?:\d+\.?\s*)?\*{0,2}"
    r"(?:references?|bibliography|works\s+cited|"
    r"citations?|sources)"
    r":?\*{0,2}\s*:?\s*$")
# Author-prefix heuristic: a line starting with "Lastname,
# F." or "Lastname, F.M." -- the APA citation pattern. Used
# to filter out blank lines + non-citation noise inside a
# references block.
_CITATION_LINE_RE = re.compile(
    r"^\s*([A-Z][a-z'\-]+(?:\s+[A-Z][a-z'\-]+)*),?\s*"
    r"(?:[A-Z]\.\s*)?(?:[A-Z]\.\s*)?")


# June 29 2026 (Issue 2) -- citation-shaped line detector. Matches
# an APA citation line that starts with "Lastname, F. M. (1234)..."
# Used by the headerless-reference-block stripper below to recognise
# trailing reference entries the LLM emitted without a "References"
# header.
_FULL_CITATION_RE = re.compile(
    r"^\s*[A-Z][a-z'\-]+(?:[\s,&]+[A-Z]\.?[a-zA-Z'\-]*)*"
    r"\s*[^()]*?\(\d{4}[a-z]?\)")
# Horizontal-rule line (markdown ---). Triggers headerless-ref-
# block detection when followed by citation-shaped lines.
_HORIZONTAL_RULE_RE = re.compile(
    r"^\s*(?:\*{3,}|-{3,}|_{3,})\s*$")


def _extract_references(narrative: str) -> tuple[str, list[str]]:
    """Split `narrative` into (body_text, citation_lines).
    Body is the narrative with any References/Bibliography
    block stripped. Citation lines are the non-blank citation
    entries from the block, with leading bullets/numbering
    trimmed.

    Detection mirrors untoken_numeric_check._strip_references_
    sections: a line matching _REFS_HEADING_RE starts a block;
    the block extends to the next non-citation-shaped line OR
    end-of-text. Fail-open: no references heading found
    returns (narrative, []).

    June 29 2026 (Issue 2 residual) -- the LLM emits inline
    reference blocks WITHOUT a "References" header in some
    sections, typically: a horizontal rule (---) followed by 2+
    citation-shaped lines at the end of the section. This
    function now runs a SECOND pass after the header-based
    detection: any trailing run of consecutive citation-shaped
    lines at the end of the body (optionally separated from the
    rest by a horizontal rule or blank lines) is moved to the
    refs list. The threshold is 2+ lines so an in-prose
    parenthetical citation doesn't get stripped."""
    if not narrative:
        return ("", [])
    lines = narrative.splitlines()
    body: list[str] = []
    refs: list[str] = []
    in_block = False
    for ln in lines:
        if in_block:
            stripped = ln.strip()
            if not stripped:
                continue
            # Stop the block when we hit another heading-shaped
            # line that ISN'T a continuation of a citation.
            if (stripped.startswith("#")
                    or _REFS_HEADING_RE.match(stripped)):
                in_block = False
                if not _REFS_HEADING_RE.match(stripped):
                    body.append(ln)
                continue
            # Trim leading "1. " / "- " / "* " bullet/numbering
            # prefixes so citations dedupe by content.
            cleaned = re.sub(
                r"^\s*(?:[-*•]|\d+\.)\s+", "", stripped)
            # Also strip the ** wrapper if the LLM bolded.
            cleaned = _HEADING_BOLD_WRAP_RE.sub(
                "", cleaned).strip()
            if cleaned:
                refs.append(cleaned)
            continue
        if _REFS_HEADING_RE.match(ln.strip()):
            in_block = True
            continue
        body.append(ln)

    # SECOND PASS (Issue 2 residual). Scan body backwards for a
    # trailing run of citation-shaped lines. Eligible trailing
    # lines are dropped from body + appended to refs. A
    # horizontal rule or blank line(s) immediately before the
    # run is also dropped to keep paragraph spacing clean.
    trailing_refs: list[str] = []
    while body:
        last = body[-1].strip()
        if not last:
            # Trailing blank line -- drop unconditionally so we
            # can keep scanning backwards.
            body.pop()
            continue
        if _FULL_CITATION_RE.match(last):
            cleaned = re.sub(
                r"^\s*(?:[-*•]|\d+\.)\s+", "", last)
            cleaned = _HEADING_BOLD_WRAP_RE.sub(
                "", cleaned).strip()
            if cleaned:
                trailing_refs.append(cleaned)
            body.pop()
            continue
        # Stop on first non-citation, non-blank line. Also drop
        # a trailing horizontal rule if it's the boundary.
        if _HORIZONTAL_RULE_RE.match(last) and trailing_refs:
            body.pop()
            continue
        break
    # Only treat the trailing block as references when there are
    # >= 2 entries -- a single in-prose Author (YYYY) citation
    # could otherwise be mis-stripped. The header-based detector
    # above is responsible for 1-entry blocks since it requires
    # an explicit heading.
    if len(trailing_refs) >= 2:
        refs.extend(reversed(trailing_refs))
    else:
        # Restore the trailing lines we tentatively removed.
        for entry in reversed(trailing_refs):
            body.append(entry)

    return ("\n".join(body), refs)


