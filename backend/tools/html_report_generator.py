def _render_strategy_table(strategy_results: dict[str, Any]) -> str:
    """
    Renders Table 1 — strategy comparison sorted by Sharpe descending.

    Same column set as the .docx generator so the appendix and the brief
    show identical numbers (a grader cross-referencing the two should
    never see disagreement).
    """
    if not strategy_results:
        return ""

    sorted_rows = sorted(
        strategy_results.items(),
        key=lambda kv: float(kv[1].get("sharpe_ratio", 0.0) or 0.0),
        reverse=True,
    )

    rows_html = []
    for name, r in sorted_rows:
        gates = r.get("tier1_gates_passed")
        gates_cell = f"{gates}/5" if gates is not None else "—"
        is_sig = bool(r.get("is_significant"))
        sig_cls = "pass" if is_sig else "warn" if (gates or 0) >= 3 else "fail"
        rows_html.append(
            "<tr>"
            f"<td>{_escape(name.replace('_', ' '))}</td>"
            f"<td class='metric'>{_fmt_pct(r.get('cagr'))}</td>"
            f"<td class='metric'>{_fmt_num(r.get('sharpe_ratio'))}</td>"
            f"<td class='metric'>{_fmt_pct(r.get('max_drawdown'))}</td>"
            f"<td class='metric'>{_fmt_p(r.get('p_value_corrected'))}</td>"
            f"<td class='metric'>{_fmt_num(r.get('cv_stability_score'))}</td>"
            f"<td class='metric {sig_cls}'>{gates_cell}</td>"
            "</tr>"
        )

    return (
        "<h3>Table 1 — Strategy comparison</h3>"
        "<table>"
        "<thead><tr>"
        "<th>Strategy</th><th>CAGR</th><th>Sharpe</th>"
        "<th>Max DD</th><th>p (FDR)</th><th>CV</th><th>Tier 1</th>"
        "</tr></thead>"
        f"<tbody>{''.join(rows_html)}</tbody>"
        "</table>"
        "<p class='table-caption'>"
        "Note. All p-values reflect Benjamini-Hochberg FDR correction "
        "across the ten-strategy universe. Tier 1 gates (out of 5) "
        "indicate cumulative statistical robustness."
        "</p>"
    )


def _render_provenance_table(registry: list[dict[str, Any]] | None) -> str:
    """Renders the data series registry as an APA-style provenance table."""
    if not registry:
        return ""
    rows_html = []
    for s in registry:
        rows_html.append(
            "<tr>"
            f"<td>{_escape(s.get('series_id'))}</td>"
            f"<td>{_escape(s.get('display_name'))}</td>"
            f"<td>{_escape(s.get('source_type'))}</td>"
            f"<td>{_escape(s.get('frequency'))}</td>"
            f"<td class='metric'>{_escape(s.get('row_count'))}</td>"
            "</tr>"
        )
    return (
        "<h3>Table 2 — Data provenance registry</h3>"
        "<table>"
        "<thead><tr>"
        "<th>Series ID</th><th>Display name</th><th>Source type</th>"
        "<th>Frequency</th><th>Rows</th>"
        "</tr></thead>"
        f"<tbody>{''.join(rows_html)}</tbody>"
        "</table>"
        "<p class='table-caption'>"
        "Note. Every value in this study traces to exactly one row of "
        "this registry. Excel-provided series are authoritative; "
        "external fetches (yfinance, FRED, Ken French) are used only "
        "where the Excel file does not provide coverage. See cross-"
        "validation results below for equity reconciliation."
        "</p>"
    )


def _render_section(heading: str, body: str) -> str:
    """Wraps a heading and prose body in HTML. Pre-escapes the body so the
    Academic Writer's plain-text output renders correctly even when the
    prose contains < or > characters."""
    paragraphs = []
    for p in body.strip().split("\n\n"):
        p = p.strip()
        if not p:
            continue
        paragraphs.append(f"<p>{_escape(p)}</p>")
    return f"<h2>{_escape(heading)}</h2>\n" + "\n".join(paragraphs)


def build_html_report(
    *,
    title: str,
    subtitle: str | None,
    sections: list[dict[str, str]],
    strategy_results: dict[str, Any] | None = None,
    provenance_registry: list[dict[str, Any]] | None = None,
    references: list[str] | None = None,
) -> str:
    """
    Builds a single self-contained HTML string.

    Strategy table is inserted after the section whose heading begins
    with "Statistical Results" or "Results" so prose can reference
    "Table 1" naturally. Provenance table is inserted after "Data Sources".

    Args:
        title:               Document title.
        subtitle:            Optional subtitle below the title.
        sections:            Ordered [{heading, body}] list.
        strategy_results:    Optional — when provided, injects Table 1.
        provenance_registry: Optional — when provided, injects Table 2.
        references:          Optional APA reference strings (one per line).
    """
    section_html_parts: list[str] = []
    table_inserted = False
    provenance_inserted = False

    for sec in sections:
        heading = sec.get("heading", "Untitled Section")
        body = sec.get("body", "")
        section_html_parts.append(_render_section(heading, body))

        # Insert Table 1 after the Results section.
        if (
            strategy_results
            and not table_inserted
            and (heading.lower().startswith("statistical results")
                 or heading.lower().startswith("results"))
        ):
            section_html_parts.append(_render_strategy_table(strategy_results))
            table_inserted = True

        # Insert Table 2 after the Data Sources section.
        if (
            provenance_registry
            and not provenance_inserted
            and heading.lower().startswith("data sources")
        ):
            section_html_parts.append(_render_provenance_table(provenance_registry))
            provenance_inserted = True

    # Catch-all: append tables at end if their host sections weren't named.
    if strategy_results and not table_inserted:
        section_html_parts.append(_render_strategy_table(strategy_results))
    if provenance_registry and not provenance_inserted:
        section_html_parts.append(_render_provenance_table(provenance_registry))

    if references:
        refs_html = "".join(f"<li>{_escape(r)}</li>" for r in references)
        section_html_parts.append(
            f"<h2>References</h2><ul class='ref-list'>{refs_html}</ul>"
        )

    generated_at = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
    subtitle_html = (
        f"<p class='subtitle'>{_escape(subtitle)}</p>" if subtitle else ""
    )

    return (
        "<!DOCTYPE html>\n"
        "<html lang='en'>\n<head>\n"
        "<meta charset='utf-8'>\n"
        f"<title>{_escape(title)}</title>\n"
        f"<style>{_CSS}</style>\n"
        "</head>\n<body>\n"
        "<div class='ai-draft-banner'>"
        f"<strong>{_AI_DRAFT_BANNER_TEXT}</strong>"
        f"<small>{_AI_DRAFT_SUBTITLE}</small>"
        "</div>\n"
        "<main>\n"
        f"<h1>{_escape(title)}</h1>\n"
        f"{subtitle_html}\n"
        f"<p class='generated'>Generated {generated_at}</p>\n"
        + "\n".join(section_html_parts)
        + "\n</main>\n</body>\n</html>"
    )
