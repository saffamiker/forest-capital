"""
tools/html_report_generator.py

Builds a self-contained HTML deliverable from a list of section dicts.
Used by /api/reports/analytical-appendix — the Analytical Appendix is
35% of the grade and the grader needs a single openable file with no
external dependencies.

Why HTML rather than .docx for the appendix:
  The appendix renders the full data provenance registry, the entire
  strategy comparison table, all five sanity assertions, and the
  reference list — content that exceeds what Word's auto-numbering
  and table styles handle cleanly at scale. HTML gives us deterministic
  control over the layout, the document is one file (no docx zip
  internals to inspect), and the grader can View Source to audit
  exactly what was generated. The AI DRAFT banner is a sticky div
  rather than a Word header.

The builder is intentionally unopinionated about content. The caller
passes ordered sections + an optional strategy_results dict + an
optional provenance registry, and the builder assembles a single
HTML string. No JavaScript — print-friendly out of the box.
"""
from __future__ import annotations

import html
from datetime import datetime, timezone
from typing import Any


