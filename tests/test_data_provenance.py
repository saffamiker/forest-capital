def test_provenance_json_has_series_key():
    data = _load_provenance()
    assert "series" in data, "provenance.json missing 'series' key"


def test_provenance_json_has_cross_validation_key():
    data = _load_provenance()
    assert "cross_validation" in data, "provenance.json missing 'cross_validation' key"


def test_provenance_json_series_have_required_fields():
    registry = _get_data_series_registry()
    required = {"series_id", "display_name", "source_type", "source_detail", "frequency", "loaded_at"}
    for s in registry:
        missing = required - set(s.keys())
        assert not missing, f"Series '{s.get('series_id', '?')}' missing fields: {missing}"


def test_provenance_json_source_types_are_valid():
    valid = {"excel_provided", "yfinance", "fred_api", "ken_french", "constant"}
    registry = _get_data_series_registry()
    for s in registry:
        assert s["source_type"] in valid, (
            f"Series '{s['series_id']}' has invalid source_type: '{s['source_type']}'"
        )


def test_provenance_json_frequencies_are_valid():
    valid = {"daily", "monthly", "quarterly"}
    registry = _get_data_series_registry()
    for s in registry:
        assert s["frequency"] in valid, (
            f"Series '{s['series_id']}' has invalid frequency: '{s['frequency']}'"
        )


