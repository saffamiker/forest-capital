# ── Helpers ────────────────────────────────────────────────────────────────────

def _load_provenance() -> dict:
    """Load the provenance.json generated by the pipeline, if it exists."""
    provenance_path = Path(__file__).parent.parent / "backend" / "data" / "provenance.json"
    if not provenance_path.exists():
        pytest.skip("provenance.json not yet generated — run get_full_history() first")
    return json.loads(provenance_path.read_text(encoding="utf-8"))


def _get_data_series_registry() -> list[dict]:
    """Returns the list of series records from provenance.json."""
    data = _load_provenance()
    return data.get("series", [])


# ── Mandatory CLAUDE.md test ───────────────────────────────────────────────────

def test_provenance_matches_actual_source():
    """
    MANDATORY: from CLAUDE.md Section 4b.
    Sprint 2 does not close until this test passes.

    Verifies:
    - excel_provided series reference the Excel file with a sheet name
    - yfinance series are only for SPY — BND and HYG must not appear
    - fred_api series contain VIXCLS and DGS2 — not DGS10 (DGS10 is in Excel)
    """
    registry = _get_data_series_registry()
    assert len(registry) > 0, "data_series_registry is empty"

    excel_series = [s for s in registry if s["source_type"] == "excel_provided"]
    for series in excel_series:
        detail = series["source_detail"]
        assert detail.get("file") == "FNA_670_Project_Sources.xlsx", (
            f"Series '{series['series_id']}' has wrong file: {detail.get('file')}"
        )
        assert detail.get("sheet") is not None, (
            f"Series '{series['series_id']}' is missing sheet name"
        )
        assert detail["sheet"] != "", (
            f"Series '{series['series_id']}' has empty sheet name"
        )

    yfinance_series = [s for s in registry if s["source_type"] == "yfinance"]
    for series in yfinance_series:
        detail = series["source_detail"]
        ticker = detail.get("ticker", "")
        # SPY is the primary yfinance ticker (equity daily).
        # LQD is the pre-BND IG bridge (2002-07 to 2007-04); it is the only
        # permitted exception to the SPY-only rule because no Excel series covers
        # the 2002-2007 IG gap and LQD tracks the same benchmark family as BND.
        assert ticker in ("SPY", "LQD"), (
            f"yfinance series '{series['series_id']}' has ticker '{ticker}' — "
            "only SPY (equity daily) and LQD (pre-BND IG bridge) are permitted"
        )

    # BND and HYG must NEVER appear as yfinance-sourced series
    yfinance_tickers = [s["source_detail"].get("ticker") for s in yfinance_series]
    assert "BND" not in yfinance_tickers, "BND appears in yfinance series — must come from Excel"
    assert "HYG" not in yfinance_tickers, "HYG appears in yfinance series — must come from Excel"

    fred_series = [s for s in registry if s["source_type"] == "fred_api"]
    fred_ids = [s["source_detail"].get("series_id", "") for s in fred_series]
    assert "VIXCLS" in fred_ids, "VIXCLS must be in fred_api series"
    assert "DGS2" in fred_ids, "DGS2 must be in fred_api series"
    assert "DGS10" not in fred_ids, (
        "DGS10 appears in fred_api series — DGS10 comes from Excel, not FRED"
    )


