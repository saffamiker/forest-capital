"""build_notebook.py -- one-shot generator for
analytical_appendix.ipynb.

Notebooks are JSON. Hand-editing is fragile (no syntax check
on cell boundaries, easy to break with a stray quote). This
script defines each cell as a Python list of source lines,
serialises the standard .ipynb v4 envelope, and writes the
file. Re-run after any structural change.

Run:
    python build_notebook.py

Output:
    analytical_appendix.ipynb (overwritten in repo root)
"""
